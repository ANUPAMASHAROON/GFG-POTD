class Solution {
  public:
    int countSubstring(string &s) {
        int cnt = 0;
        int n = s.length();
        
        unordered_map<char, int> map;
        for(auto ch : s){
            cnt += map[ch];
            map[ch]++;
        }
        return (n+cnt);
    }
};


